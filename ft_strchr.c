char	*ft_strchr(const char *s, int c)
{
	int		i;
	char	*res;

	i = 0;
	while (s[i] != '\0' && s[i] != c)
		i++;
	if (s[i] == c && s[i] != '\0')
	{
		res = (char *) s + i;
		return (res);
	}
	else
		return (0);
}
